                "Highway 61 Revisited" é o sexto álbum de estúdio de Bob Dylan, lançado em agosto de 1965. Este álbum é frequentemente considerado uma obra-prima e é um marco crucial na carreira de Dylan, representando sua transição do folk para o rock.

A história por trás de "Highway 61 Revisited" envolve uma mudança significativa no som e na abordagem musical de Bob Dylan. Antes deste álbum, Dylan era conhecido principalmente por seu trabalho no movimento folk, com letras politicamente engajadas e uma abordagem acústica.

O título do álbum refere-se à Highway 61, uma rodovia que corre ao longo do rio Mississippi, conectando o sul dos Estados Unidos. Essa rodovia tem uma importância simbólica na história da música, especialmente no blues. O álbum foi em grande parte gravado nos estúdios Columbia, em Nova York, com a produção de Bob Johnston.

"Highway 61 Revisited" é notável pela inclusão de músicos de rock de estúdio, como Mike Bloomfield e Al Kooper, que contribuíram para a sonoridade elétrica e mais robusta do álbum. A faixa de abertura, "Like a Rolling Stone", é uma das canções mais conhecidas e influentes de Dylan, e sua mudança para uma abordagem elétrica marcou um ponto de virada em sua carreira.

A letra de "Like a Rolling Stone" é considerada uma das melhores já escritas e é conhecida por sua atitude desafiadora e poesia surrealista. O álbum também inclui outras faixas notáveis, como "Ballad of a Thin Man", "Highway 61 Revisited" e "Desolation Row", que demonstram a habilidade de Dylan em contar histórias e explorar temas sociais e culturais.

"Highway 61 Revisited" recebeu aclamação da crítica e é considerado um dos álbuns mais importantes da história do rock. A mudança de Dylan para um som mais elétrico foi inicialmente polêmica entre seus fãs de folk, mas o álbum acabou influenciando significativamente o desenvolvimento do rock contemporâneo. Ele é frequentemente citado como uma influência crucial para músicos e bandas que seguiram, ajudando a definir a transição do folk para o rock nos anos 1960.
