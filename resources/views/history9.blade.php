            "Ten" é o álbum de estreia da banda de rock americana Pearl Jam, lançado em 27 de agosto de 1991. Este álbum é frequentemente considerado um dos álbuns essenciais da década de 1990 e é um marco na cena do rock alternativo.

A história por trás de "Ten" está fortemente ligada à formação do Pearl Jam. A banda se originou em Seattle, no estado de Washington, e fazia parte da cena grunge que emergiu na região no início dos anos 90. O álbum foi produzido por Rick Parashar e a banda, e foi gravado nos estúdios London Bridge em Seattle.

"Ten" é notável por seu som poderoso e emotivo, caracterizado pelas letras intensas de Eddie Vedder, a voz distintiva do vocalista, e os riffs de guitarra de Stone Gossard e Mike McCready. O álbum recebeu uma recepção inicial modesta, mas ganhou impulso significativo à medida que as músicas foram tocadas nas rádios e o videoclipe de "Jeremy" ganhou destaque na MTV.

Algumas das faixas mais conhecidas de "Ten" incluem "Alive", "Even Flow", "Jeremy", "Black" e "Oceans". Cada uma dessas músicas apresenta elementos do grunge, mas também incorpora influências de rock clássico e hard rock, proporcionando uma abordagem única ao som alternativo da época.

A capa do álbum, que apresenta uma foto da arte "Axtell 1989", de Ament, tornou-se icônica e é frequentemente associada à imagem do Pearl Jam.

"Ten" eventualmente alcançou enorme sucesso comercial, atingindo multi-platina nos Estados Unidos. Ele ajudou a solidificar o Pearl Jam como uma das bandas mais importantes e duradouras da cena grunge. A sinceridade das letras de Vedder, a intensidade musical e a autenticidade da banda ressoaram com uma ampla audiência, e o álbum teve um impacto duradouro na música alternativa.

Ao longo dos anos, "Ten" continuou a ser celebrado como um dos melhores álbuns da década de 1990 e um marco na história do rock. O Pearl Jam construiu uma carreira consistente desde então, mas "Ten" permanece como um ponto de referência crucial em sua discografia e na evolução do rock alternativo.</p>
