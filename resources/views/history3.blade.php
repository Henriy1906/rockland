                Led Zeppelin IV", também conhecido como "Untitled" ou "Four Symbols", é o quarto álbum de estúdio da banda britânica de rock Led Zeppelin. Foi lançado em 8 de novembro de 1971. Assim como os álbuns anteriores da banda, "Led Zeppelin IV" foi um grande sucesso e é considerado um dos melhores álbuns de rock de todos os tempos.

O álbum não tem um título oficial, mas é frequentemente chamado de "Led Zeppelin IV" por ser o quarto álbum da banda. A capa do álbum é famosa por não ter o nome da banda nem o título do álbum, apresentando apenas quatro símbolos misteriosos, um para cada membro da banda.

A gravação do álbum ocorreu em vários estúdios, incluindo Headley Grange e Island Studios, ambos no Reino Unido. A produção foi feita pelos membros da banda, principalmente pelo guitarrista Jimmy Page. A abordagem do Led Zeppelin para o álbum foi mais experimental, incorporando uma variedade de estilos musicais, desde o blues até o folk e o hard rock.

O álbum apresenta algumas das faixas mais icônicas e duradouras do Led Zeppelin, incluindo "Stairway to Heaven", uma das músicas mais conhecidas da história do rock. A composição de "Stairway to Heaven", escrita por Robert Plant e Jimmy Page, é notável pela sua complexidade musical e lírica. A música tornou-se um hino do rock clássico e é frequentemente citada como uma das maiores canções já gravadas.

Outras faixas notáveis incluem "Black Dog", "Rock and Roll" e "When the Levee Breaks". Cada uma dessas músicas demonstra a habilidade do Led Zeppelin em fundir diversos estilos musicais, resultando em um som distintivo e inovador.

"Led Zeppelin IV" é amplamente aclamado pela crítica e pelos fãs e é considerado um marco na história do rock. A complexidade musical, as letras poéticas e a produção inovadora contribuíram para sua reputação duradoura como um dos álbuns mais influentes e bem-sucedidos da história do rock.
