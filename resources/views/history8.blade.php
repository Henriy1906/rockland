            <p class="text-justify">
            "Nevermind" é o segundo álbum de estúdio da banda de rock Nirvana, lançado em 24 de setembro de 1991. Este álbum é frequentemente considerado um dos mais influentes da história do rock e um marco crucial na popularização do gênero grunge.

A história por trás de "Nevermind" está intrinsecamente ligada à ascensão meteórica do Nirvana à fama e ao impacto cultural que a banda teve no início dos anos 90. O álbum foi produzido por Butch Vig e gravado em Sound City Studios, em Van Nuys, Califórnia.

O single "Smells Like Teen Spirit" foi lançado antes do álbum e teve um impacto explosivo na cena musical da época. A canção tornou-se um hino da geração X e ajudou a popularizar o grunge, um subgênero do rock alternativo, caracterizado por suas guitarras distorcidas, letras muitas vezes introspectivas e uma abordagem mais "suja" em comparação com o som do rock dominante na época.

"Nevermind" apresenta uma mistura de músicas que vão desde as mais agressivas, como "Breed" e "Territorial Pissings", até as mais melódicas e acessíveis, como "Come as You Are" e "Lithium". A diversidade musical do álbum, juntamente com as letras pessoais e muitas vezes enigmáticas de Kurt Cobain, contribuiu para sua ampla aceitação e impacto duradouro.

A capa do álbum, que apresenta um bebê submerso em uma piscina atrás de uma nota de dólar, também se tornou icônica. Ela capturou a atenção do público e contribuiu para a imagem rebelde e anti-establishment associada ao Nirvana.

"Nevermind" desencadeou uma onda de popularidade para o Nirvana, elevando-os à fama mundial. O álbum atingiu o primeiro lugar nas paradas e eventualmente foi certificado como diamante nos Estados Unidos. No entanto, o sucesso meteórico também trouxe desafios para a banda, especialmente para Kurt Cobain, que lutou contra a pressão da fama.

Infelizmente, a história do Nirvana é marcada pela tragédia, com o falecimento de Kurt Cobain em 1994. Apesar disso, o legado de "Nevermind" e do Nirvana continua vivo, e o álbum é frequentemente citado como um dos melhores e mais influentes da história do rock. Ele é considerado um marco na transição do rock mainstream dos anos 80 para a explosão do rock alternativo nos anos 90. </div>
